def add(num1: float, num2: float):
    result = num1 + num2
    return result

def sub(num1: float, num2: float):
    result = num1 - num2
    return result

def mul(num1: float, num2: float):
    result = num1 * num2
    return result
    
def div(num1: float, num2: float):
    result = num1 / num2
    return result

